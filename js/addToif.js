//switch-case

const point = 12;

switch (point) {
    case 1, 2:
        console.log('you have 1');
        break;
    case 3, 4, 5:
        console.log('you have 2');
        break;
    case 6: 
        console.log('you have 3');
        break;
    case 7, 8, 9: 
        console.log('you have 4');
        break;
    case 10, 11, 12: 
        console.log('you have 5');
        break;
    default:
        console.log('it\'s not a mark');
}